style.font.name = "Calibri"
style.font.size = Pt(11)


def H(text: str, level: int = 1):
    p = doc.add_heading(text, level=level)
    for r in p.runs:
        r.font.color.rgb = RGBColor(0x1F, 0x3A, 0x5F)
    return p


def P(text: str = "", bold: bool = False, italic: bool = False):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.bold = bold
    r.italic = italic
    return p


def UL(items: list[str]):
    for it in items:
        doc.add_paragraph(it, style="List Bullet")


def tag(p, text, color=RGBColor(0x6B, 0x6B, 0x6B), size=9):
    r = p.add_run(f"  {text}")
    r.font.color.rgb = color
    r.font.size = Pt(size)
    r.italic = True


